#include "ft_printf.h"









int     ft_putDec(int n)
{

    if (n == -2147483648)
    {
        ft_putstr("-2147483648");
        return (1);
    }
    if (n < 0)
    {
        ft_putchar('-');
        n = -n;
    }
    if (n >= 10)
    {
        ft_putDec(n / 10);
        ft_putDec(n % 10);
    }
    else
    {
        ft_putchar(n + '0');
  
    }
    return (1);
}
